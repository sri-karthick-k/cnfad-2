package com.example.ciefirst;

public class Ticket {
    int id, seatNo, price;
    String type;

    @Override
    public String toString() {
        return "Ticket{" +
                "id=" + id +
                ", seatNo=" + seatNo +
                ", price=" + price +
                ", type='" + type + '\'' +
                '}';
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSeatNo() {
        return seatNo;
    }

    public void setSeatNo(int seatNo) {
        this.seatNo = seatNo;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
